REPLACEMENTS.sort((a, b) => b[0].length - a[0].length);

function protectChunks(html) {
  const chunks = [];
  let safe = html.replace(/<script[\s\S]*?<\/script>/gi, (m) => {
    const i = chunks.length;
    chunks.push(m);
    return `\x00SCRIPT${i}\x00`;
  });
  safe = safe.replace(/<style[\s\S]*?<\/style>/gi, (m) => {
    const i = chunks.length;
    chunks.push(m);
    return `\x00STYLE${i}\x00`;
  });
  return { safe, chunks };
}

function restoreChunks(safe, chunks) {
  return safe.replace(/\x00(?:SCRIPT|STYLE)(\d+)\x00/g, (_, i) => chunks[Number(i)] || '');
}

function protectUrlQueries(html, chunks) {
  return html.replace(
    /(\.(?:css|js|png|jpe?g|webp|svg|woff2?|ico))(\?v=[^"'\s>]+)/gi,
    (m, ext, q) => {
      const i = chunks.length;
      chunks.push(q);
      return ext + `\x00URLQ${i}\x00`;
    }
  );
}

function fixHtml(html) {
  const { safe, chunks } = protectChunks(html);
  let out = protectUrlQueries(safe, chunks);
  for (const [from, to] of REPLACEMENTS) {
    out = out.split(from).join(to);
  }
  out = out.replace(/ci\?n/gi, (m) => (m[0] === 'c' ? 'ción' : 'CIÓN'));
  out = out.replace(/si\?n/gi, (m) => (m[0] === 's' ? 'sión' : 'SIÓN'));
  out = out.replace(/([aAeEiIoOuU])\?([a-z])/g, (_, v, next) => {
    const map = { a: 'á', A: 'Á', e: 'é', E: 'É', i: 'í', I: 'Í', o: 'ó', O: 'Ó', u: 'ú', U: 'Ú' };
    return (map[v] || v) + next;
  });
  out = out.replace(/n\?([aeiou])/gi, (m, v) => 'ñ' + v);
  out = out.replace(/(^|[\s(>«"'])s\?(?=[\s<.,;:!?)»"']|$)/g, '$1sí');
  out = out.replace(/(^|[\s(>«"'])S\?(?=[\s<.,;:!?)»"']|$)/g, '$1Sí');
  out = restoreChunks(out, chunks);
  return out.replace(/\x00URLQ(\d+)\x00/g, (_, i) => chunks[Number(i)] || '');
}

let html = readFileSync(indexPath, 'utf8');
const before = (html.match(/\?/g) || []).length;
html = fixHtml(html);
const after = (html.match(/\?/g) || []).length;
writeFileSync(indexPath, html, 'utf8');
console.log(`index.html: ? antes=${before} después=${after}`);
